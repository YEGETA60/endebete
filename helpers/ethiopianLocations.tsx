export function getLocationCoordinates(location: string): { lat: number; lng: number } | null {
  // Direct match
  if (LOCATION_COORDINATES[location]) return LOCATION_COORDINATES[location];
  
  // Partial match - check if location starts with or contains a known key
  // e.g. "Addis Ababa, Kazanchis" should match "Addis Ababa - Kazanchis" or "Addis Ababa"
  const normalized = location.replace(/,\s*/g, ' - ').trim();
  if (LOCATION_COORDINATES[normalized]) return LOCATION_COORDINATES[normalized];
  
  // Try matching just the city part
  for (const [key, coords] of Object.entries(LOCATION_COORDINATES)) {
    if (location.toLowerCase().includes(key.toLowerCase()) || key.toLowerCase().includes(location.toLowerCase())) {
      return coords;
    }
  }
  
  return null;
}

